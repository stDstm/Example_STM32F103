  HAL_ADCEx_Calibration_Start(&hadc1);
  uint16_t adc_buf[COUNT_REQUEST] = {0,};
  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adc_buf, COUNT_REQUEST);

  uint32_t adc_ch_1 = 0;
