void trans_to_usart_data(char *buf)
{
	uint8_t len = strlen((char*)buf);

	for(uint8_t i = 0; i < len; i++)
	{
	  while((USART3->SR & USART_SR_TXE) == 0){}
	  USART3->DR = buf[i];
	}

	while((USART3->SR & USART_SR_TXE) == 0){}
	USART3->DR = '\n';
}


